# 1)მომხმარებელს შემოატანინეთ 2 რიცხვი და მათზე მოახდინეთ ყველა მათემატიკური ოპერაცია
num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))

print(f"\nრიცხვების ჯამი: {num1} + {num2} = {num1 + num2}")
print(f"რიცხვების სხვაობა: {num1} - {num2} = {num1 - num2}")
print(f"რიცხვების ნამრავლი: {num1} * {num2} = {num1 * num2}")
print(f"რიცხვების გაყოფა: {num1} / {num2} = {num1 / num2}")
print(f"ნაშთიანი გაყოფა: {num1} // {num2} = {num1 % num2}")